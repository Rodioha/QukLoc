# Функция для получения информации о конкретном месте
def get_place_info(place_name):
    # Сначала ищем в существующих данных
    for main_place, places in nearby_places.items():
        for place in places:
            if place["name"].lower() == place_name.lower():
                return place
    
    # Если не нашли, возвращаем место по умолчанию
    return {
        "name": place_name,
        "description": "Информация об этом месте скоро появится!",
        "photo": "../images/default.jpg"
    }

# Дополнительная функция для поиска по частичным совпадениям
def find_main_place_by_partial_name(partial_name):
    """
    Находит полное название места по частичному совпадению
    """
    partial_name = partial_name.lower()
    for main_place in nearby_places.keys():
        if partial_name in main_place.lower():
            return main_place
    return None

