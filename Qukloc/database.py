def get_nearby_places(place_name):
    # Приводим к нижнему регистру для нечеткого поиска
    search_name = place_name.lower()
    
    # Ищем частичное совпадение в ключах
    for main_place in nearby_places.keys():
        if search_name in main_place.lower() or main_place.lower() in search_name:
            return nearby_places[main_place]
    
    # Если не нашли, возвращаем места по умолчанию
